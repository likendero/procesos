    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int resultado = fileChooser.showOpenDialog(this);
        // Comprobacion que la seleccionde del fichero es correcta
        if(resultado == JFileChooser.APPROVE_OPTION){
            // se almacena en el recuadro el directorio seleccionado
            txDirectorio.setText(fileChooser.getSelectedFile().getAbsolutePath());
        }
        
    }//GEN-LAST:event_btBuscarActionPerformed

    private void btCifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCifrarActionPerformed
        int seleccion = 0;
        // switch que cambia segun el radio button
        if(rd128.isSelected()){
            seleccion = 1;
        }else if(rd256.isSelected()){
            seleccion = 2;
        }else{
            seleccion = 3;
        }
        // comprovacion de que el directorio no esta vacio
        if(!txDirectorio.getText().isEmpty()){
            File ficheroEntrada, ficheroSalida;
            ficheroEntrada = new File(txDirectorio.getText());
            String aux = ficheroEntrada.getAbsolutePath() + ".cod";
            ficheroSalida = new File(aux);
            String clave = txClave.getText().trim();
            Cifrar.cifrarFichero(ficheroEntrada, ficheroSalida, clave, seleccion);
        }
    }//GEN-LAST:event_btCifrarActionPerformed

