    private void btDesencriptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDesencriptarActionPerformed
       int seleccion = 0;
        // switch que cambia segun el radio button
        if(rd128.isSelected()){
            seleccion = 1;
        }else if(rd256.isSelected()){
            seleccion = 2;
        }else{
            seleccion = 3;
        }
        // comprovacion de que el directorio no esta vacio
        if(!txDirectorio.getText().isEmpty()){
            File ficheroEntrada, ficheroSalida;
            ficheroEntrada = new File(txDirectorio.getText());
            String aux = ficheroEntrada.getAbsolutePath().replace(".cod", "");
            ficheroSalida = new File(aux);
            String clave = txClave.getText().trim();
            Descifrar.descifrarFichero(ficheroEntrada, ficheroSalida, clave, seleccion);
        }
    }//GEN-LAST:event_btDesencriptarActionPerformed

